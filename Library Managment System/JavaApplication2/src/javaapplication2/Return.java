    private void tbl_returnMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_returnMousePressed
        // TODO add your handling code here:
        cb_Items.removeAllItems(); // to make sure cmbo box is clear after every click
        var userId = (tbl_return.getValueAt(tbl_return.getSelectedRow(), 0).toString());
        // setting text field and combo box
        for (int i = 0; i < lb_.users.size(); i++) {
            if(lb_.users.get(i).userId.equals(userId)){
                edt_UserID.setText(lb_.users.get(i).userId);
                edt_Name.setText(lb_.users.get(i).name);
                for (int j = 0; j < lb_.users.get(i).borrowedItem.size(); j++) {
                    cb_Items.addItem(lb_.users.get(i).borrowedItem.get(j).getTitle());
                }
            }
        }
    }//GEN-LAST:event_tbl_returnMousePressed

