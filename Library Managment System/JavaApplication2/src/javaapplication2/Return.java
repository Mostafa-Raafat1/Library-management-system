    private void btn_returnItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_returnItemActionPerformed
        // TODO add your handling code here:
        //setting the process of returning
        if(!edt_UserID.getText().isEmpty() && !edt_Name.getText().isEmpty() && cb_Items.getSelectedIndex() != -1 ){
            for (int i = 0; i < lb_.users.size() ; i++) {
                if(lb_.users.get(i).userId.equals(edt_UserID.getText())){
                    for (int j = 0; j < lb_.users.get(i).borrowedItem.size(); j++) {
                        if(lb_.users.get(i).borrowedItem.get(j).getTitle().equals(cb_Items.getSelectedItem().toString())){
                            lb_.returnProcess(lb_.users.get(i), lb_.users.get(i).borrowedItem.get(j));
                            JOptionPane.showMessageDialog(this,"Item has been successfully returned");
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btn_returnItemActionPerformed

